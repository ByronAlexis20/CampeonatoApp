package ec.edu.upse.gcf.auxiliar;

public class AuxTotalEquipoLocal {
	
	private String grupo;
	private Integer posicionEquipoLocal;
	private Integer totalEncuentro;
	
	public AuxTotalEquipoLocal() {
		
	}

	public String getGrupo() {
		return grupo;
	}

	public void setGrupo(String grupo) {
		this.grupo = grupo;
	}

	public Integer getPosicionEquipoLocal() {
		return posicionEquipoLocal;
	}

	public void setPosicionEquipoLocal(Integer posicionEquipoLocal) {
		this.posicionEquipoLocal = posicionEquipoLocal;
	}

	public Integer getTotalEncuentro() {
		return totalEncuentro;
	}

	public void setTotalEncuentro(Integer totalEncuentro) {
		this.totalEncuentro = totalEncuentro;
	}
	
	

}
