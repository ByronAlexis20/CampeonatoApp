package ec.edu.upse.gcf.auxiliar;

/**
 * Clase auxiliar para los estados de las fechas
 * para el campeonato
 * @author Dayana Tigrero
 *
 */
public class AuxFecha {

	private Integer auxMes;
	private Integer auxDia;
	
	public AuxFecha() {
		
	}

	public Integer getAuxMes() {
		return auxMes;
	}

	public void setAuxMes(Integer auxMes) {
		this.auxMes = auxMes;
	}

	public Integer getAuxDia() {
		return auxDia;
	}

	public void setAuxDia(Integer auxDia) {
		this.auxDia = auxDia;
	}
	
	
	
}
