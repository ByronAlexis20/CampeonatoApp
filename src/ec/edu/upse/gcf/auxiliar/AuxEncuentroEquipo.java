package ec.edu.upse.gcf.auxiliar;

public class AuxEncuentroEquipo {
 
	private Integer equipoLocal;
	private Integer posicionEquipoLocal;
	private Integer equipoVisitante;
	private Integer posicionEquipoVisitante; 
	
	public AuxEncuentroEquipo() {
		
	}

	public Integer getEquipoLocal() {
		return equipoLocal;
	}

	public void setEquipoLocal(Integer equipoLocal) {
		this.equipoLocal = equipoLocal;
	}

	public Integer getPosicionEquipoLocal() {
		return posicionEquipoLocal;
	}

	public void setPosicionEquipoLocal(Integer posicionEquipoLocal) {
		this.posicionEquipoLocal = posicionEquipoLocal;
	}

	public Integer getEquipoVisitante() {
		return equipoVisitante;
	}

	public void setEquipoVisitante(Integer equipoVisitante) {
		this.equipoVisitante = equipoVisitante;
	}

	public Integer getPosicionEquipoVisitante() {
		return posicionEquipoVisitante;
	}

	public void setPosicionEquipoVisitante(Integer posicionEquipoVisitante) {
		this.posicionEquipoVisitante = posicionEquipoVisitante;
	}

	 
}
